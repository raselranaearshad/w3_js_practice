// charAt() method
let text = "Hello World";
document.write(text.charAt(6) + "<br>");

// charCodeAt() Method
let txt = "Hello Bangladesh";
document.write(txt.charCodeAt(7) + "<br>");

// at() Method
let x = "W3School";
document.write(x.at(2) + "<br>");

// Property Access [] Method
let y = "W3School";
document.write(y[1] + '<br>');

// Slice Method
let fruits = "Apple, Banana, Orange"
document.write(fruits.slice(7, 13) + "<br>");

//String substring() Method
let str = "Apple, Banana, Orange";
document.write(str.substring(0, 5) + "<br>");

// String substr() Method
let sT = 'Mango, Strawberry, Papaya';
document.write(sT.substr(7, 10) + "<br>");

// String toUpperCase()
let texT = "Hello World";
document.write(texT.toUpperCase("Hello World") + "<br>");

// String toLowerCase()
let text1 = "Bangladesh";
document.write(text1.toLowerCase("Bangladesh"));