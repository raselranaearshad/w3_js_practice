// Back-Tics Syntax
let x = `Hello World`;
document.write(x + "<br>");

// Quotes Inside String
let text = `He's often called "Johnny"`;
document.write(text + "<br>");

/* String Interpolation or Variable Substitutions */
let firstName = "Bilkiss";
let lastName = "Akter";
// It's called variable substitution
let result = `Welcome ${firstName} ${lastName}.`;
document.write(result + "<br>");