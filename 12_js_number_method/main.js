// The toString() Method
let strNum = 123;
document.write(strNum.toString() + "<br>");

// The toExponential() Method
let expNum = 9.66;
document.write(expNum.toExponential(4) + "<br>");

// The toFixed() Method
let fixNum = 9.69;
document.write(fixNum.toFixed(3) + "<br>");

// The toPrecision() Method
let preNum = 6.369;
document.write(preNum.toPrecision(2) + "<br>");

// The valueOf() Method
let valNum = 256;
document.write(valNum.valueOf() + "<br>");

// The Number() Method
let x = Number("10,20");
document.write(x + "<br>");

// The Number() Method Used on Dates
let X = new Date("2004-10-20");
document.write(Number(X) + "<br>");

// The parseInt() Method
let y = parseInt('-10.22');
document.write(y + "<br>");

// The parseFloat() Method
let Y = parseFloat("10.5 years");
document.write(Y + "<br>");