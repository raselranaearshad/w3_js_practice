// The toString() Method
let strNum = 123;
document.write(strNum.toString() + "<br>");

// The toExponential() Method
let expNum = 9.66;
document.write(expNum.toExponential(4) + "<br>");

// The toFixed() Method
let fixNum = 9.69;
document.write(fixNum.toFixed(3) + "<br>");

// The toPrecision() Method
let preNum = 6.369;
document.write(preNum.toPrecision(2) + "<br>");

// The valueOf() Method
let valNum = 256;
document.write(valNum.valueOf() + "<br>");
